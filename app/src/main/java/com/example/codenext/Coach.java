package com.example.codenext;


public class Coach {
    String coachname;
    String coachemail;

    public Coach(String coachname, String coachemail) {
        this.coachname = coachname;
        this.coachemail = coachemail;
    }

    public String getCoachname() {
        return coachname;
    }

    public String getCoachemail() {
        return coachemail;
    }
}

